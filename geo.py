
import math

PI = 3.141592653589793

def perimeter(radius):
    return 2 * radius * PI

def area(radius):
    return PI * radius**2

def sin(degree):
    radians = math.radians(degree)
    return math.sin(radians)

def cos(degree):
    radians = math.radians(degree)
    return math.cos(radians)





